#!/usr/bin/env node
/* eslint-disable no-console */

const { spawn, spawnSync, execSync } = require('child_process');
const fs = require('fs');
const net = require('net');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const ARTIFACT_DIR = path.join(ROOT, 'artifacts');
const METRO_LOG = path.join(ARTIFACT_DIR, 'smoke-fresh-metro.log');
const GENERATED_FLAG_PATH = path.join(
  ROOT,
  'src',
  'generated',
  'smokeBootstrapFlag.ts',
);
const APP_ID = 'com.localfootyathlete.app';
const RELEVANT_LOG_TERMS = [
  'app-entry',
  'smoke-bootstrap',
  'navigation-state',
  'tabs-mounted',
  'tab-press',
  'nav-route',
  'coach-screen',
  'coach-ready',
  'coach-build',
  'Linking',
];
const REQUIRED_MARKERS = [
  '[app-entry] App.tsx module loaded',
  '[smoke-bootstrap] installer imported',
  '[smoke-bootstrap] installer mounted',
  '[smoke-bootstrap] addEventListener("url") attached',
];

const INERT_GENERATED_FLAG_SOURCE = [
  '// Generated by scripts/smoke-coach-bike-flow-fresh.js for the live smoke.',
  '// Keep the committed default inert.',
  'export const SMOKE_BOOTSTRAP_FLOW: string | null = null;',
  '',
].join('\n');

const ACTIVE_GENERATED_FLAG_SOURCE = [
  '// Generated by scripts/smoke-coach-bike-flow-fresh.js for the live smoke.',
  '// This file is reset to null when the fresh smoke exits.',
  "export const SMOKE_BOOTSTRAP_FLOW: string | null = 'coach-bike-flow';",
  '',
].join('\n');

function header(label) {
  console.log(`\n-- ${label} --`);
}

function info(label) {
  console.log(`  • ${label}`);
}

function ok(label) {
  console.log(`  ✓ ${label}`);
}

function fail(label, detail) {
  console.error(`\n✗ ${label}`);
  if (detail) console.error(detail);
  const err = new Error(label);
  err.smokeFailureAlreadyReported = true;
  process.exitCode = 1;
  throw err;
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function ensureArtifactDir() {
  if (!fs.existsSync(ARTIFACT_DIR)) {
    fs.mkdirSync(ARTIFACT_DIR, { recursive: true });
  }
}

function writeGeneratedSmokeFlag(active) {
  fs.mkdirSync(path.dirname(GENERATED_FLAG_PATH), { recursive: true });
  fs.writeFileSync(
    GENERATED_FLAG_PATH,
    active ? ACTIVE_GENERATED_FLAG_SOURCE : INERT_GENERATED_FLAG_SOURCE,
  );
}

function getFreePort() {
  return new Promise((resolve, reject) => {
    const server = net.createServer();
    server.on('error', reject);
    server.listen(0, '127.0.0.1', () => {
      const address = server.address();
      const port = typeof address === 'object' && address ? address.port : 0;
      server.close(() => resolve(port));
    });
  });
}

function commandExists(cmd) {
  try {
    execSync(`command -v ${cmd}`, { stdio: ['ignore', 'ignore', 'ignore'] });
    return true;
  } catch {
    return false;
  }
}

function projectCwdForPid(pid) {
  if (process.platform !== 'darwin' || !commandExists('lsof')) return '';
  try {
    const out = execSync(`lsof -a -p ${pid} -d cwd -Fn`, {
      stdio: ['ignore', 'pipe', 'ignore'],
    }).toString();
    const line = out.split(/\r?\n/).find((entry) => entry.startsWith('n'));
    return line ? line.slice(1) : '';
  } catch {
    return '';
  }
}

function findProjectDevServerPids() {
  try {
    const out = execSync('ps -axo pid=,command=', {
      stdio: ['ignore', 'pipe', 'ignore'],
    }).toString();
    return out
      .split(/\r?\n/)
      .map((line) => {
        const match = line.trim().match(/^(\d+)\s+(.*)$/);
        return match ? { pid: Number(match[1]), command: match[2] } : null;
      })
      .filter(Boolean)
      .filter(({ pid, command }) => {
        if (pid === process.pid) return false;
        if (!/(expo|@expo\/cli|metro|react-native).*start|metro/i.test(command)) {
          return false;
        }
        const cwd = projectCwdForPid(pid);
        return cwd === ROOT;
      })
      .map(({ pid }) => pid);
  } catch {
    return [];
  }
}

async function killExistingProjectDevServers() {
  header('Kill existing project Metro/Expo processes');
  const pids = findProjectDevServerPids();
  if (pids.length === 0) {
    ok('No project dev server processes found.');
    return;
  }
  for (const pid of pids) {
    try {
      process.kill(pid, 'SIGTERM');
      info(`SIGTERM ${pid}`);
    } catch {}
  }
  await sleep(2000);
  ok(`Stopped ${pids.length} project dev server process(es).`);
}

function simulatorLogPredicate() {
  const termPredicates = RELEVANT_LOG_TERMS.map(
    (term) => `eventMessage CONTAINS[c] "${term}"`,
  );
  return [
    ...termPredicates,
    'process CONTAINS[c] "LocalFooty"',
    'processImagePath CONTAINS[c] "LocalFooty"',
    'processImagePath CONTAINS[c] "localfootyathlete"',
  ].join(' OR ');
}

function startSimulatorLogCapture(logStream) {
  if (process.platform !== 'darwin' || !commandExists('xcrun')) {
    return {
      text: () => '',
      stop: () => {},
    };
  }

  const lines = [];
  let buffer = '';
  const child = spawn(
    'xcrun',
    [
      'simctl',
      'spawn',
      'booted',
      'log',
      'stream',
      '--style',
      'compact',
      '--level',
      'debug',
      '--predicate',
      simulatorLogPredicate(),
    ],
    { cwd: ROOT, stdio: ['ignore', 'pipe', 'pipe'] },
  );

  const onData = (chunk) => {
    const text = chunk.toString();
    logStream.write(text);
    buffer += text;
    const parts = buffer.split(/\r?\n/);
    buffer = parts.pop() || '';
    for (const line of parts) {
      if (line.trim()) lines.push(line);
      if (lines.length > 2000) lines.shift();
    }
  };

  child.stdout.on('data', onData);
  child.stderr.on('data', onData);

  return {
    text() {
      return lines.join('\n');
    },
    stop() {
      try {
        child.kill('SIGTERM');
      } catch {}
    },
  };
}

function spawnLogged(command, args, env, logStream) {
  const child = spawn(command, args, {
    cwd: ROOT,
    env: { ...process.env, ...env },
    stdio: ['ignore', 'pipe', 'pipe'],
  });
  child.stdout.on('data', (chunk) => {
    process.stdout.write(chunk);
    logStream.write(chunk);
  });
  child.stderr.on('data', (chunk) => {
    process.stderr.write(chunk);
    logStream.write(chunk);
  });
  return child;
}

function runLogged(command, args, env, logStream) {
  return new Promise((resolve) => {
    const child = spawnLogged(command, args, env, logStream);
    child.on('close', (status) => resolve(status));
  });
}

function markerSnapshot(text) {
  return {
    appEntry: /\[app-entry\] App\.tsx module loaded/.test(text),
    bootstrapSignalVisible:
      /smokeBootstrapEnv["']?\s*[:=]\s*["']coach-bike-flow/.test(text) ||
      /smokeBootstrapFileFlag["']?\s*[:=]\s*["']coach-bike-flow/.test(text) ||
      /smokeBootstrapSignal["']?\s*[:=]\s*["']coach-bike-flow/.test(text) ||
      /SMOKE_BOOTSTRAP_FLOW[^\n]*coach-bike-flow/.test(text) ||
      /EXPO_PUBLIC_SMOKE_BOOTSTRAP[^\n]*coach-bike-flow/.test(text),
    installerImported: /\[smoke-bootstrap\] installer imported/.test(text),
    installerMounted: /\[smoke-bootstrap\] installer mounted/.test(text),
    listenerAttached: /\[smoke-bootstrap\] addEventListener\("url"\) attached/.test(
      text,
    ),
    envFlagDetected: /\[smoke-bootstrap\] env flag detected flow=coach-bike-flow/.test(
      text,
    ),
    fileFlagDetected: /\[smoke-bootstrap\] file flag detected flow=coach-bike-flow/.test(
      text,
    ),
    bootstrapStarted: /\[smoke-bootstrap\] started flow=coach-bike-flow/.test(text),
    profileSeeded: /\[smoke-bootstrap\] profile seeded/.test(text),
    programInstalled: /\[smoke-bootstrap\] program installed/.test(text),
    onboardingComplete: /\[smoke-bootstrap\] onboarding complete/.test(text),
    navigationTrue: /\[navigation-state\] isOnboardingComplete true/.test(text),
    tabsMounted: /\[tabs-mounted\] true/.test(text),
  };
}

function formatSnapshot(snapshot) {
  return [
    `  [app-entry] App.tsx module loaded:                         ${snapshot.appEntry ? 'yes' : 'NO'}`,
    `  bootstrap flag visible at runtime:                         ${snapshot.bootstrapSignalVisible ? 'yes' : 'NO'}`,
    `  [smoke-bootstrap] installer imported:                      ${snapshot.installerImported ? 'yes' : 'NO'}`,
    `  [smoke-bootstrap] installer mounted:                       ${snapshot.installerMounted ? 'yes' : 'NO'}`,
    `  [smoke-bootstrap] addEventListener("url") attached:         ${snapshot.listenerAttached ? 'yes' : 'NO'}`,
    `  [smoke-bootstrap] env flag detected flow=coach-bike-flow:   ${snapshot.envFlagDetected ? 'yes' : 'NO'}`,
    `  [smoke-bootstrap] file flag detected flow=coach-bike-flow:  ${snapshot.fileFlagDetected ? 'yes' : 'NO'}`,
    `  [smoke-bootstrap] started flow=coach-bike-flow:             ${snapshot.bootstrapStarted ? 'yes' : 'NO'}`,
    `  [smoke-bootstrap] profile seeded:                          ${snapshot.profileSeeded ? 'yes' : 'NO'}`,
    `  [smoke-bootstrap] program installed:                       ${snapshot.programInstalled ? 'yes' : 'NO'}`,
    `  [smoke-bootstrap] onboarding complete:                     ${snapshot.onboardingComplete ? 'yes' : 'NO'}`,
    `  [navigation-state] isOnboardingComplete true:               ${snapshot.navigationTrue ? 'yes' : 'NO'}`,
    `  [tabs-mounted] true:                                       ${snapshot.tabsMounted ? 'yes' : 'NO'}`,
  ].join('\n');
}

function readCombinedRuntimeText(simCapture) {
  const metroText = fs.existsSync(METRO_LOG) ? fs.readFileSync(METRO_LOG, 'utf8') : '';
  return `${metroText}\n${simCapture.text()}`;
}

async function waitForRuntimeMarkers(simCapture, timeoutMs) {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    const text = readCombinedRuntimeText(simCapture);
    const snapshot = markerSnapshot(text);
    const flagDetected = snapshot.envFlagDetected || snapshot.fileFlagDetected;
    if (
      snapshot.appEntry &&
      snapshot.bootstrapSignalVisible &&
      snapshot.installerImported &&
      snapshot.installerMounted &&
      snapshot.listenerAttached &&
      flagDetected &&
      snapshot.bootstrapStarted &&
      snapshot.profileSeeded &&
      snapshot.programInstalled &&
      snapshot.onboardingComplete &&
      snapshot.navigationTrue &&
      snapshot.tabsMounted
    ) {
      return snapshot;
    }
    await sleep(500);
  }
  return markerSnapshot(readCombinedRuntimeText(simCapture));
}

function terminateAppIfPossible() {
  if (process.platform !== 'darwin' || !commandExists('xcrun')) return;
  spawnSync('xcrun', ['simctl', 'terminate', 'booted', APP_ID], {
    stdio: ['ignore', 'ignore', 'ignore'],
  });
}

function isDevBuildInstalled() {
  if (process.platform !== 'darwin' || !commandExists('xcrun')) return false;
  const result = spawnSync(
    'xcrun',
    ['simctl', 'get_app_container', 'booted', APP_ID, 'app'],
    {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'pipe'],
    },
  );
  return result.status === 0 && result.stdout.trim().length > 0;
}

function openDevClientUrl(port) {
  if (process.platform !== 'darwin' || !commandExists('xcrun')) return;
  const url = `${APP_ID}://expo-development-client/?url=${encodeURIComponent(
    `http://127.0.0.1:${port}`,
  )}`;
  info(`Opening dev client URL: ${url}`);
  const result = spawnSync('xcrun', ['simctl', 'openurl', 'booted', url], {
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'pipe'],
  });
  if (result.status !== 0) {
    fail(
      'Fresh smoke setup failed.',
      [
        'Could not open the dev-client URL against the fresh Metro server.',
        `URL: ${url}`,
        `stderr: ${result.stderr || '(empty)'}`,
      ].join('\n'),
    );
  }
}

async function ensureDevBuildInstalled(smokeEnv, logStream) {
  if (isDevBuildInstalled()) {
    ok('Dev build is already installed on the booted simulator.');
    return;
  }

  info('Dev build not installed on the booted simulator; building/installing.');
  const runStatus = await runLogged(
    'npx',
    ['expo', 'run:ios', '--no-bundler'],
    smokeEnv,
    logStream,
  );

  if (runStatus === 0) {
    ok('Dev build installed.');
    return;
  }

  if (isDevBuildInstalled()) {
    info(
      '`npx expo run:ios --no-bundler` exited non-zero after install; continuing with manual dev-client launch.',
    );
    return;
  }

  fail(
    'Fresh smoke setup failed.',
    [
      '`npx expo run:ios --no-bundler` failed before the dev build was installed.',
      `Metro/setup log: ${METRO_LOG}`,
      '',
      'Manual fallback:',
      '  npm run smoke:coach-bike-flow:fresh',
    ].join('\n'),
  );
}

async function main() {
  console.log('\n┌──────────────────────────────────────────────────┐');
  console.log('│  smoke:coach-bike-flow:fresh                      │');
  console.log('└──────────────────────────────────────────────────┘');

  ensureArtifactDir();
  fs.writeFileSync(METRO_LOG, '');
  writeGeneratedSmokeFlag(true);

  await killExistingProjectDevServers();

  const port = Number(process.env.SMOKE_METRO_PORT || (await getFreePort()));

  header('Start Metro with clean cache and env bootstrap');
  info(`Metro port: ${port}`);
  info('Metro env: EXPO_PUBLIC_SMOKE_BOOTSTRAP=coach-bike-flow');
  info(`Generated smoke flag: ${GENERATED_FLAG_PATH}`);
  info(`Metro log: ${METRO_LOG}`);
  const logStream = fs.createWriteStream(METRO_LOG, { flags: 'a' });
  const simCapture = startSimulatorLogCapture(logStream);
  const smokeEnv = {
    EXPO_PUBLIC_SMOKE_BOOTSTRAP: 'coach-bike-flow',
    SMOKE_BOOTSTRAP_DELIVERY: 'env',
    SMOKE_RUNTIME_PREFLIGHT_ALREADY_PROVEN: '1',
  };
  const metro = spawnLogged(
    'npx',
    ['expo', 'start', '--dev-client', '--localhost', '--port', String(port), '-c'],
    smokeEnv,
    logStream,
  );

  try {
    await sleep(5000);

    header('Ensure dev build is installed and launched');
    terminateAppIfPossible();
    await ensureDevBuildInstalled(smokeEnv, logStream);
    openDevClientUrl(port);

    header('Wait for runtime installer markers');
    const snapshot = await waitForRuntimeMarkers(simCapture, 60_000);
    if (!snapshot.appEntry) {
      fail(
        'App runtime logs were not captured, or simulator connected to a different Metro bundle.',
        [
          'The fresh smoke started its own Metro server, but never observed App.tsx executing.',
          `Expected dev-client URL: http://127.0.0.1:${port}`,
          `Metro/setup log: ${METRO_LOG}`,
          '',
          'Observed runtime markers:',
          formatSnapshot(snapshot),
        ].join('\n'),
      );
    }
    if (!snapshot.installerImported) {
      fail(
        'App.tsx loaded but smokeBootstrapInstaller was not imported.',
        [`Metro/setup log: ${METRO_LOG}`, '', formatSnapshot(snapshot)].join('\n'),
      );
    }
    if (!snapshot.bootstrapSignalVisible) {
      fail(
        'Installer present but bootstrap flag missing from runtime.',
        [`Metro/setup log: ${METRO_LOG}`, '', formatSnapshot(snapshot)].join('\n'),
      );
    }
    if (!snapshot.envFlagDetected && !snapshot.fileFlagDetected) {
      fail(
        'Bootstrap flag visible, but bootstrap hook did not detect it.',
        [`Metro/setup log: ${METRO_LOG}`, '', formatSnapshot(snapshot)].join('\n'),
      );
    }
    if (!snapshot.bootstrapStarted) {
      fail(
        'Bootstrap flag detected, but bootstrap did not start.',
        [`Metro/setup log: ${METRO_LOG}`, '', formatSnapshot(snapshot)].join('\n'),
      );
    }
    if (!snapshot.listenerAttached) {
      fail(
        'App.tsx loaded but smoke bootstrap listener was not attached.',
        [`Metro/setup log: ${METRO_LOG}`, '', formatSnapshot(snapshot)].join('\n'),
      );
    }
    if (!snapshot.onboardingComplete && snapshot.bootstrapStarted) {
      fail(
        'Bootstrap started but failed before completing onboarding.',
        [`Metro/setup log: ${METRO_LOG}`, '', formatSnapshot(snapshot)].join('\n'),
      );
    }
    ok('Runtime markers appeared in captured app logs.');
    console.log(formatSnapshot(snapshot));

    header('Run pipeline + Maestro smoke through env bootstrap');
    // Prove that the generated bootstrap flag is still pointed at
    // coach-bike-flow at the moment Maestro launches. The fresh wrapper
    // wrote the active flag before starting Metro and never resets it
    // until the finally block below — but log it so that any future
    // edit that resets the flag mid-run leaves a paper trail in the
    // smoke log.
    {
      const flagSource = fs.readFileSync(GENERATED_FLAG_PATH, 'utf8');
      const flagActive = /SMOKE_BOOTSTRAP_FLOW[^\n]*['"]coach-bike-flow['"]/.test(
        flagSource,
      );
      if (!flagActive) {
        fail(
          'Generated smoke bootstrap flag was reset before Maestro started.',
          [
            'The fresh wrapper expected SMOKE_BOOTSTRAP_FLOW to remain `coach-bike-flow` until after Maestro completes,',
            `but ${GENERATED_FLAG_PATH} no longer contains it.`,
            '',
            flagSource.trim(),
          ].join('\n'),
        );
      }
      console.log('  • [smoke-flag] generated flag still set before Maestro');
      logStream.write('[smoke-flag] generated flag still set before Maestro\n');
    }
    const smokeStatus = await runLogged(
      'node',
      ['scripts/smoke-coach-bike-flow.js', '--env-bootstrap'],
      smokeEnv,
      logStream,
    );
    if (smokeStatus !== 0) {
      fail('Fresh smoke failed.', `Metro/setup log: ${METRO_LOG}`);
    }
    ok('Fresh smoke passed.');
  } finally {
    simCapture.stop();
    try {
      metro.kill('SIGTERM');
    } catch {}
    // Reset is done ONLY here, after the smoke script returned (success
    // or failure). This is the inverse of the [smoke-flag] still-set
    // log we emitted before runLogged: those two markers bracket the
    // entire Maestro-driven launch, so a missing "still set" + missing
    // "reset after" pair tells us exactly when the flag flipped.
    writeGeneratedSmokeFlag(false);
    console.log('  • [smoke-flag] generated flag reset after Maestro');
    try {
      logStream.write('[smoke-flag] generated flag reset after Maestro\n');
    } catch {}
    logStream.end();
  }
}

main().catch((err) => {
  if (err?.smokeFailureAlreadyReported) {
    process.exit(process.exitCode || 1);
  }
  console.error('\n✗ Fresh smoke setup failed.');
  console.error(err?.stack || String(err));
  process.exit(1);
});
